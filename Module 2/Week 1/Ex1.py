# Câu nào sau đây là đúng để tạo mảng 1 chiều từ 0 đến 9

import numpy as np
arr_1 = np . arange (0 , 10 , 1)

arr_2 = np . arange (1 , 10 , 1)

arr_3 = np . arange (0 , 9 , 1)

arr_4 = np . arange (1 , 11 , 1)

# Câu trả lời đúng: arr_1 = np . arange (0 , 10 , 1)

# Gỉai thích: np.arange(start, stop, step) tạo mảng 1 chiều từ start đến stop với bước step.
# Ví dụ: np.arange(0, 10, 1) tạo mảng 1 chiều từ 0 đến 9 với bước 1.